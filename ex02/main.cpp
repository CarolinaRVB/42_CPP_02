#include "Fixed.hpp"
#include <iostream>

int main( void ) {
	Fixed a;
	Fixed const b( Fixed( 5.05f ) * Fixed( 2 ) );
	
	std::cout << a << std::endl;
	std::cout << ++a << std::endl;
	std::cout << a << std::endl;
	std::cout << a++ << std::endl;
	std::cout << a << std::endl;
	std::cout << b << std::endl;
	
	// Instance vs. Class Context: Non-static member functions require an instance
	// to be called, while static member functions do not.
	
	std::cout << Fixed::max( a, b ) << std::endl;
	return 0;
}